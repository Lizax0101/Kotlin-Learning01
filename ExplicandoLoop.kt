fun main() {
    var i = 0
    //enquanto
    while (i < 5) {
        println(i)
        i++
    }
}
/**
 * Então a cada execução i assume um valor que é exibido na tela e em seguida +1 é adicionado a valor atual, sendo
 * i= 0  é menor que 5 - true
 * i= 1 é menor que 5 - true
 * i= 2 é menor que 5 - true
 * i= 3 é menor que 5 - true
 * i= 4 é menor que 5 - true
 * i= 5 é menor que 5 ? não. 5 É IGUAL A 5, então FALSE.
 * Aqui o loop é encerrado, pois o while só e executado enquanto a condição for verdadeira.
 */
